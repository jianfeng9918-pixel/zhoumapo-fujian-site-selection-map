def download_geojson():
    geo_dir = ROOT / "data" / "geojson"
    geo_dir.mkdir(parents=True, exist_ok=True)
    targets = {"350000": ROOT / "data" / "fujian-350000-full.json"}
    targets.update({code: geo_dir / f"{code}_full.json" for code in CITY_CODES.values()})
    for code, dest in targets.items():
        url = f"https://geo.datav.aliyun.com/areas_v3/bound/{code}_full.json"
        try:
            if not dest.exists() or dest.stat().st_size < 1000:
                urlretrieve(url, dest)
        except Exception as exc:
            print(f"GeoJSON下载失败 {code}: {exc}")


