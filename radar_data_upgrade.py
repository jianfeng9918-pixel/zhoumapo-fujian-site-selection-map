def value(value):
    if value is None:
        return ""
    return str(value).strip()


def ensure_sheet(wb, title, headers):
    if title in wb.sheetnames:
        ws = wb[title]
    else:
        ws = wb.create_sheet(title)
        ws.append(headers)
    ensure_headers(ws, headers)
    return ws


def ensure_headers(ws, headers):
    existing = [value(cell.value) for cell in ws[1]]
    if not any(existing):
        ws.append(headers)
        existing = [value(cell.value) for cell in ws[1]]
    for header in headers:
        if header not in existing:
            ws.cell(row=1, column=len(existing) + 1).value = header
            existing.append(header)
    for cell in ws[1]:
        cell.font = Font(bold=True, color="FFFFFF")
        cell.fill = PatternFill("solid", fgColor="17754F")


def header_map(ws):
    return {value(cell.value): idx + 1 for idx, cell in enumerate(ws[1])}


def delete_generated(ws, prefixes):
    for row in range(ws.max_row, 1, -1):
        key = value(ws.cell(row=row, column=1).value)
        if any(key.startswith(prefix) for prefix in prefixes):
            ws.delete_rows(row)


def delete_generated_scores(ws):
    headers = header_map(ws)
    note_col = headers.get("说明", 8)
    for row in range(ws.max_row, 1, -1):
        note = value(ws.cell(row=row, column=note_col).value)
        if "雷达版批量评分" in note:
            ws.delete_rows(row)


def append_records(ws, records):
    headers = header_map(ws)
    for record in records:
        row_idx = ws.max_row + 1
        for key, val in record.items():
            if key not in headers:
                ensure_headers(ws, [key])
                headers = header_map(ws)
            ws.cell(row=row_idx, column=headers[key]).value = val


